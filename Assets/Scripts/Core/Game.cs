using UnityEngine;

namespace Medieval.Core
{
    public class Game : MonoBehaviour
    {
        public static Game instance;

        private void Awake()
        {
            if (instance == null)
            {
                instance = this;
                DontDestroyOnLoad(gameObject);
            }
            else
            {
                Destroy(gameObject);
            }
        }
    }
}